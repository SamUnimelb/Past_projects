    private void recordBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordBtActionPerformed
        // TODO add your handling code here:
        rUserBMI = new RegisteredUserBMI();
        rUserBMI.setRUserID(rUserID);
        rUserBMI.setHeight(Double.parseDouble(heightField.getText()));
        rUserBMI.setWeight(Double.parseDouble(weightField.getText()));
        rUserBMILinker = new RUserBMILinker();
        BMIavg = rUserBMI.getWeight() / Math.pow(rUserBMI.getHeight(), 2);
        newBMIDisplayLabel.setText(String.format("%.2f", BMIavg));
        newBodyStatusDisplayLabel.setText(judgeBodyStatus(BMIavg));
        rUserBMILinker.setRUserBMI(rUserBMI);
        String hint = "Add your record " + 
                ( (rUserBMILinker.addUserBMIToDB())? " success. " : " fails. ");
        JOptionPane.showMessageDialog(this, hint);
        reloadUserBMIData(rUserID);
        refreshUserBMIStatus();
    }//GEN-LAST:event_recordBtActionPerformed

    private void cleanBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cleanBtActionPerformed
        // TODO add your handling code here:
        rUserBMI = new RegisteredUserBMI();
        rUserBMI.setRUserID(rUserID);
        boolean cleanSuccess = rUserBMILinker.cleanUserBMIInfo();
        String hint = "Clean your record "
                + (cleanSuccess ? " success. " : " fails. ");

        JOptionPane.showMessageDialog(this, hint);

        if (cleanSuccess)
            JOptionPane.showMessageDialog(this, "Your record is cleared, please input new record!", "Warning!", JOptionPane.WARNING_MESSAGE);
    }//GEN-LAST:event_cleanBtActionPerformed

