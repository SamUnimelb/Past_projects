    public String judgeBodyStatus(double BMI){
        if(BMI <= 18.5)
            return "Too thin";
        else if(BMI <= 24)
            return "Normal";
        else if(BMI <= 27.5)
            return "Too fat";
        else
            return "Obesity";
    }//end method

