    private void updateFoodDrinkList() {
        foodDrinkLinker = new FoodDrinkLinker();
        foodDrinkList = foodDrinkLinker.getAllFoodDrink();
        foodDrinkStrList = new LinkedList<>();
        foodDrinkList.forEach((fd) -> {
            foodDrinkStrList.add(fd.getFoodDrinkName() + "-" + fd.getFoodDrinkChinesename());
        });
        foodDrinkStrs = (String[]) foodDrinkStrList.toArray(new String[foodDrinkStrList.size()]);
        if (foodJCombo != null) {
            foodJCombo.setModel(new javax.swing.DefaultComboBoxModel<>(foodDrinkStrs));
        }
    }
    
    private void updateAmountUnitList(){
        amountMeasurementLinker = new AmountMeasurementLinker();
        amountUnitList = amountMeasurementLinker.getAllMeasurements();
        amountUnitStrList = new LinkedList<>();
        
        amountUnitList.forEach((au) -> {
            amountUnitStrList.add(au.getMeasurementName() + "-" + au.getMeasurementChineseName());
        });
        amountUnitStrs = (String[]) amountUnitStrList.toArray(new String[amountUnitStrList.size()]);
        if (amountCombo != null) {
            amountCombo.setModel(new javax.swing.DefaultComboBoxModel<>(amountUnitStrs));
        }
    }//end method
    
