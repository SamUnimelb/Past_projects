    private void recordBtActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_recordBtActionPerformed
        // TODO add your handling code here:
        JOptionPane.showMessageDialog(this, "Simply select another item and record the portion,"
              + "\nif you eat more than one food. ");
        
        actualDiet = new ActualDiet();
        actualDiet.setUserID(rUserID);
        foodDrink = foodDrinkList.get(foodJCombo.getSelectedIndex());
        actualDiet.setFoodDrinkID(foodDrink.getFoodDrinkID());
        amountUnit = amountUnitList.get(amountCombo.getSelectedIndex());
        actualDiet.setMeasurementID(amountUnit.getMeasurementID());
        actualDiet.setUnitAmount(Double.parseDouble(amountField.getText()));
        
        actualDietLinker = new ActualDietLinker();
        actualDietLinker.writeUserActualDiet(rUserID, actualDiet);
    }//GEN-LAST:event_recordBtActionPerformed

