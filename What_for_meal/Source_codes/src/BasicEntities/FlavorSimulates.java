package BasicEntities;

public class FlavorSimulates {

	private int flavorID;
	private int diseaseID;
	private String reason;
	private String resonChinese;

	public int getFlavorID() {
		return this.flavorID;
	}

	/**
	 * 
	 * @param flavorID
	 */
	public void setFlavorID(int flavorID) {
		this.flavorID = flavorID;
	}

	public int getDiseaseID() {
		return this.diseaseID;
	}

	/**
	 * 
	 * @param diseaseID
	 */
	public void setDiseaseID(int diseaseID) {
		this.diseaseID = diseaseID;
	}

	public String getReason() {
		return this.reason;
	}

	/**
	 * 
	 * @param reason
	 */
	public void setReason(String reason) {
		this.reason = reason;
	}

	public String getResonChinese() {
		return this.resonChinese;
	}

	/**
	 * 
	 * @param resonChinese
	 */
	public void setResonChinese(String resonChinese) {
		this.resonChinese = resonChinese;
	}

}