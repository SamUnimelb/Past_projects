package BasicEntities;

public class Experts {

	private int exptID;
	private String exptUserName;
	private String exptPassword;
	private String exptEmail;

	public int getExptID() {
		return this.exptID;
	}

	/**
	 * 
	 * @param exptID
	 */
	public void setExptID(int exptID) {
		this.exptID = exptID;
	}

	public String getExptUserName() {
		return this.exptUserName;
	}

	/**
	 * 
	 * @param exptUserName
	 */
	public void setExptUserName(String exptUserName) {
		this.exptUserName = exptUserName;
	}

	public String getExptPassword() {
		return this.exptPassword;
	}

	/**
	 * 
	 * @param exptPassword
	 */
	public void setExptPassword(String exptPassword) {
		this.exptPassword = exptPassword;
	}

	public String getExptEmail() {
		return this.exptEmail;
	}

	/**
	 * 
	 * @param exptEmail
	 */
	public void setExptEmail(String exptEmail) {
		this.exptEmail = exptEmail;
	}

}