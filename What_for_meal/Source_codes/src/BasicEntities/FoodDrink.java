package BasicEntities;

public class FoodDrink {

	private int foodDrinkID;
	private String foodDrinkName;
	private String foodDrinkChinesename;
	private double calorie;
	private double carbohydrate;
	private double fat;
	private double protein;
	private double vitamin;
	private double mineral;
	private double water;

	public int getFoodDrinkID() {
		return this.foodDrinkID;
	}

	/**
	 * 
	 * @param foodDrinkID
	 */
	public void setFoodDrinkID(int foodDrinkID) {
		this.foodDrinkID = foodDrinkID;
	}

	public String getFoodDrinkName() {
		return this.foodDrinkName;
	}

	/**
	 * 
	 * @param foodDrinkName
	 */
	public void setFoodDrinkName(String foodDrinkName) {
		this.foodDrinkName = foodDrinkName;
	}

	public String getFoodDrinkChinesename() {
		return this.foodDrinkChinesename;
	}

	/**
	 * 
	 * @param foodDrinkChinesename
	 */
	public void setFoodDrinkChinesename(String foodDrinkChinesename) {
		this.foodDrinkChinesename = foodDrinkChinesename;
	}

	public double getCalorie() {
		return this.calorie;
	}

	/**
	 * 
	 * @param calorie
	 */
	public void setCalorie(double calorie) {
		this.calorie = calorie;
	}

	public double getCarbohydrate() {
		return this.carbohydrate;
	}

	/**
	 * 
	 * @param carbohydrate
	 */
	public void setCarbohydrate(double carbohydrate) {
		this.carbohydrate = carbohydrate;
	}

	public double getFat() {
		return this.fat;
	}

	/**
	 * 
	 * @param fat
	 */
	public void setFat(double fat) {
		this.fat = fat;
	}

	public double getProtein() {
		return this.protein;
	}

	/**
	 * 
	 * @param protein
	 */
	public void setProtein(double protein) {
		this.protein = protein;
	}

	public double getVitamin() {
		return this.vitamin;
	}

	/**
	 * 
	 * @param vitamin
	 */
	public void setVitamin(double vitamin) {
		this.vitamin = vitamin;
	}

	public double getMineral() {
		return this.mineral;
	}

	/**
	 * 
	 * @param mineral
	 */
	public void setMineral(double mineral) {
		this.mineral = mineral;
	}

	public double getWater() {
		return this.water;
	}

	/**
	 * 
	 * @param water
	 */
	public void setWater(double water) {
		this.water = water;
	}

}